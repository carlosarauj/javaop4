package operadores03;
import java.util.Scanner;

public class Principal {

	public static void main(String[] args) {
		int num1, num2;
		
		num1 = 6;
		num2 = 6;
		
		num1++;
		++num2;
		
		System.out.println(num1);
		System.out.println(num2);
		
		num1++;
		++num2;


		System.out.println(num1);
		System.out.println(num2);
		
		int num3;
		
		num3 = 6;
		num3 += 4;
		
		System.out.println(num3);
	}

}
